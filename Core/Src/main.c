  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_LPUART1_UART_Init();
  MX_SPI1_Init();
  /* USER CODE BEGIN 2 */

  //FA FF 30 00 D1
  spi_tx_buf[0] = PRE;
  spi_tx_buf[1] = BID;
  spi_tx_buf[2] = 0x30;
  spi_tx_buf[3] = 0x00;
  spi_tx_buf[4] = 0xD1;

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
